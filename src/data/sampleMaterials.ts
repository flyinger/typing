export async function sampleMaterialPacks(): Promise<MaterialPack[]> {
  const items: ExerciseItem[] = [];

  for (const [index, raw] of rawItems.entries()) {
    const itemHash = await hashText(stableStringify(raw));
    items.push({
      ...raw,
      id: `builtin_item_${String(index + 1).padStart(2, "0")}_${itemHash.slice(0, 10)}`,
      source: "TypingLab 内置材料",
      contentHash: itemHash,
    });
  }

  return [
    {
      id: "pack_builtin_foundation",
      name: "TypingLab 启动材料",
      description: "覆盖五笔编码、真实中文、英文、代码和 Vim 的首轮样例材料。",
      version: 1,
      source: "builtin",
      createdAt: builtinMaterialTimestamp,
      updatedAt: builtinMaterialTimestamp,
      contentHash: await hashText(stableStringify(rawItems)),
      items,
    },
  ];
}
